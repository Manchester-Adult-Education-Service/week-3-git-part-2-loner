# -------------------------------------------
# Group Exercise: Collaborative Story Program
# -------------------------------------------
# Work in groups of 2 (or 3 if needed). You will build one Python file together.
# Each group member will add to the story and practice using Git collaboratively.
#
# -------------------------------------------
# Step 1: First Learner
# -------------------------------------------
# - Create (or edit) a file called story.py.
# - Add Python code that asks for the user’s name and prints a welcome message.
#
# Example (don’t just copy – personalise it!):
# name = input("What is your name? ")
# print(f"Welcome {name}, let's start a story together!")
#
# - Save the file, then commit and push:
#    git add story.py
#    git commit -m "Created base story file"
#    git push origin main
#
# -------------------------------------------
# Step 2: Second Learner
# -------------------------------------------
# - In your terminal, pull the latest changes:
#    git pull origin main
#
# - Add another question (e.g. favourite colour, favourite food, or hobby).
# - Print a response using that input.
#
# Example:
# colour = input("What is your favourite colour? ")
# print(f"Great choice, {colour} is a wonderful colour!")
#
# - Save the file, commit and push:
#    git add story.py
#    git commit -m "Added colour choice to story"
#    git push origin main
#
# -------------------------------------------
# Step 3: Third Learner (if in a group of 3)
# -------------------------------------------
# - Pull the repo:
#    git pull origin main
#
# - Add another part to the story (e.g. favourite place, favourite animal).
# - Save, commit and push your changes.
#
# -------------------------------------------
# Step 4: Everyone
# -------------------------------------------
# - Pull the repo again:
#    git pull origin main
#
# - Run the program to see your group’s full story!
#
# -------------------------------------------
# ⭐ Extension Challenge (optional)
# -------------------------------------------
# - Combine all the answers into a final story paragraph.
# - Example:
#   print(f"Welcome {name}! Your favourite colour is {colour} and you love {food}.")
#   print(f"One day, you went to {place} with a {colour} {animal} while eating {food}!")
#
# - Commit and push your extension:
#    git add story.py
#    git commit -m "Completed group story extension"
#    git push origin main
#
# -------------------------------------------
# ✅ Done!
